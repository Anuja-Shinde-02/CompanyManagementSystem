    private void populateFields() {
        for(NGO d: ecosystem.getNgoDir().getNgoList()){
            if(d.getNgoAgent().equals(ngoAname)){
            txtNGOName.setText(d.getNgoName());
            txtNGOName.setEnabled(false);
            txtNGOContact.setText(d.getNgoPhno());
            txtNGOContact.setEnabled(false);
            txtNGOAgent.setText(d.getNgoAgent());
            txtNGOAgent.setEnabled(false);
            txtNGOAddress.setText(d.getNgoAddress());
            txtNGOAddress.setEnabled(false);
            txtNGOCity.setText(d.getNgoCity());
            txtNGOCity.setEnabled(false);
            txtNGOState.setText(d.getNgoState());
            txtNGOState.setEnabled(false);
            txtNGOZipCode.setText(d.getNgoZipcode());
            txtNGOZipCode.setEnabled(false);
            txtNGOusername.setText(d.getNgoAccount().getUsername());
            txtNGOusername.setEnabled(false);
            txtNGOpassword.setText(d.getNgoAccount().getPassword());
            txtNGOpassword.setEnabled(false);
            }
        }
    }
    
