    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(!nullCheck()){
            ArrayList<NGO> ngoList = ecosystem.getNgoDir().getNgoList();
            for(NGO d: ngoList)
            {
                if(d.getNgoAccount().getUsername().equals(NGOUname))
                {
                      //The get method returns the variable name's value.
                     //The name variable is given a parameter (newName), which is assigned via the set method.
                    d.setNgoName(txtNGOName.getText());
                    d.setNgoPhno(txtNGOContact.getText());
                    d.setNgoAgent(txtNGOAgent.getText());
                    d.setNgoAddress(txtNGOAddress.getText());
                    d.setNgoCity(txtNGOCity.getText());
                    d.setNgoState(txtNGOState.getText());
                    d.setNgoZipcode(txtNGOZipCode.getText());
                    UserAccount ngoAccount=new UserAccount();
                    ngoAccount.setUsername(txtNGOusername.getText());
                    ngoAccount.setPassword(txtNGOpassword.getText());
                }
            }
            // ecosystem.setFCWDirectory(fcwList);
            ngod = ecosystem.getNgoDir();
            ecosystem.setNgoDir(ngod);
             //JOptionPane.showMessageDialog is used to create an information-message dialog titled "Message"
             //after clicking on submit the profile will be updated successfully 
            JOptionPane.showMessageDialog(this, "Profile updated successfully");
            disableFields();
        }
        else{
            //all fields should be field 
            JOptionPane.showMessageDialog(this, "All fields are mandatory!");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

